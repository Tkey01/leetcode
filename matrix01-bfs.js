const isValid = (mat, x, y) =>
  Array.isArray(mat[x]) && Number.isInteger(mat[x][y])

const getNeighbors = (mat, i, j) => {
  let res = []

  if (isValid(mat, i + 1, j)) {
    res.push({ i: i + 1, j })
  }
  if (isValid(mat, i, j + 1)) {
    res.push({ i, j: j + 1 })
  }
  if (isValid(mat, i - 1, j)) {
    res.push({ i: i - 1, j })
  }
  if (isValid(mat, i, j - 1)) {
    res.push({ i, j: j - 1 })
  }

  return res
}

const updateMatrix = (mat) => {
  let res = [[]]
  let queue = []

  for (let i = 0; i < mat.length; i++) {
    res[i] = []
    for (let j = 0; j < mat[i].length; j++) {
      if (mat[i][j] === 0) {
        res[i][j] = 0
        queue.push({ i, j })
      } else {
        res[i][j] = Number.MAX_VALUE
      }
    }
  }

  while (queue.length) {
    const curr = queue.shift()
    const neighbors = getNeighbors(mat, curr.i, curr.j)

    neighbors.forEach((n) => {
      if (res[n.i][n.j] > res[curr.i][curr.j] + 1) {
        res[n.i][n.j] = res[curr.i][curr.j] + 1
        queue.push({ i: n.i, j: n.j })
      }
    })
  }

  return res
}

