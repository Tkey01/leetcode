const updateMatrix = (mat) => {
  let res = [[]]

  for (let i = 0; i < mat.length; i++) {
    res[i] = []
    for (let j = 0; j < mat[i].length; j++) {
      res[i][j] = Number.MAX_VALUE
    }
  }

  for (let i = 0; i < mat.length; i++) {
    for (let j = 0; j < mat[i].length; j++) {
      if (mat[i][j] === 0) {
        res[i][j] = 0
      } else {
        for (let k = 0; k < mat.length; k++) {
          for (let m = 0; m < mat[k].length; m++) {
            if (mat[k][m] === 0) {
              const r = Math.min(res[i][j], Math.abs(k - i) + Math.abs(m - j))
              res[i][j] = r
            }
          }
        }
      }
    }
  }

  return res
}

